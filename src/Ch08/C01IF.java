package Ch08;

public class C01IF {

	public static void main(String[] args) {
		//01 단순 IF
		int age = 10;
		if(age >=8)
		{
			System.out.println("학교에 다닙니다");
		}
		System.out.println("첫 번째 IF 블럭 종료");
		if(age<8)
		{
			System.out.println("학교에 다니지 않습니다");
		}
		System.out.println("두 번째 IF 블럭 종료");
		System.out.println("프로그램을 종료합니다");

	}

}
